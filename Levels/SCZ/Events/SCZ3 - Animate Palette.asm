; ---------------------------------------------------------------------------
; SCZ3 palette cycling
; ---------------------------------------------------------------------------

; =============== S U B R O U T I N E =======================================

AnPal_SCZ3:
		lea	(Normal_palette_line_4+$1A).w,a1
		move.l	(a1),d0
		addi.l	#$02020202,d0
		andi.l	#$0EEE0EEE,d0
		move.l	d0,(a1)+
		move.w	(a1),d0
		addi.w	#$0202,d0
		andi.w	#$0EEE,d0
		move.w	d0,(a1)
		rts